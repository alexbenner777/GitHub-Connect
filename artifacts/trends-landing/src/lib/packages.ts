// ─── Тип категории ───────────────────────────────────────────────
export type PackageCategory = {
  id: string;
  title: string;
  items: string[];
};

// ─── Основной тип пакета ─────────────────────────────────────────
export type InvestPackage = {
  /** Уникальный идентификатор — используется в API и навигации */
  id: string;
  /** Маркетинговое название пакета */
  name: string;
  /** Короткий слоган под названием */
  tagline: string;
  /** Стоимость в USDT */
  price: number;
  /** Доля RevShare из общего пула (5 000 долей = 100% пула) */
  shares: number;
  /** Потенциальный доход/мес при 50 млн DAU, в USD */
  dau50m: number;
  /** Оценочный Exit потенциал в виде строки для отображения */
  exit: string;
  /** Название Badge в приложении */
  badge: string;
  /** Пакет рекомендован (показывается метка «ХИТ») */
  recommended?: boolean;
  /** Кол-во источников монетизации для RevShare */
  monetizationSources: number;
  /**
   * Категории преимуществ.
   * Порядок важен — блоки отображаются сверху вниз в модалке.
   */
  categories: PackageCategory[];
};

